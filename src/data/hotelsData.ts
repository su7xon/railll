export const getHotelsByCategory = (category: string): Hotel[] => {
  if (category === 'all') return hotelsData;
  return hotelsData.filter(hotel => hotel.category === category);
};

export const getHotelById = (id: number): Hotel | undefined => {
  return hotelsData.find(hotel => hotel.id === id);
};

export const searchHotels = (query: string): Hotel[] => {
  const searchTerm = query.toLowerCase();
  return hotelsData.filter(hotel => 
    hotel.name.toLowerCase().includes(searchTerm) ||
    hotel.location.toLowerCase().includes(searchTerm) ||
    hotel.description.toLowerCase().includes(searchTerm)
  );
};